	public DrawBoard()
	{

	}

	public void draw(Graphics2D g2d)
	{
		Color brown = new Color(153, 102, 0);
		Color tan = new Color(245,222,179);

		//fill background color
		g2d.setColor(tan);
		g2d.fillRect(100, 75, 400, 420);
		int x, y;
		int i = 0, j = 0;
		//draw first half of board
		for(i = 0; i < 8; i++){
			for(j = 0; j < 4; j++)
			{
				g2d.setColor(brown);
				x = i * 50 + 100;
				y = j * 50 + 75;
				g2d.drawRect(x, y, 50, 50);
			}
		}
		//draw river
		y = j * 50 + 75;
		g2d.drawRect(100, y, 400, 20);
		//draw second half of board
		for(i = 0; i < 8; i++)
		{
			int oldY = 295;
			for(j = 0; j < 4; j++)
			{
				g2d.setColor(brown);
				x = i * 50 + 100;
				g2d.drawRect(x, oldY, 50, 50);
				oldY = oldY + 50;

			}
		}
		//first X
		g2d.drawLine(250, 75, 350, 175);
		g2d.drawLine(350, 75, 250, 175);
		//second X
		g2d.drawLine(250, 395, 350, 495);
		g2d.drawLine(350, 395, 250, 495);
	}

